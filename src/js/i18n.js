let currentLanguage = 'nb';

/**
 * Detekterer systemspråk og returnerer nærmeste støttede språk
 * @returns {string} Språkkode (f.eks. 'nb', 'en')
 */
function detectSystemLanguage() {
    const browserLang = navigator.language || navigator.languages?.[0] || 'nb';
    const langCode = browserLang.split('-')[0].toLowerCase();
    
    // Spesialhåndtering for norsk: 'no' → 'nb'
    if (langCode === 'no') return 'nb';
    
    // Sjekk om språket er støttet direkte
    if (SUPPORTED_LANGUAGES[langCode]) {
        return langCode;
    }
    
    // Fallback til engelsk, deretter norsk bokmål
    return 'en';
}

/**
 * Initialiserer språksystemet
 * Bruker lagret preferanse eller systemspråk
 */
function initI18n() {
    const savedLang = localStorage.getItem('bare-language');
    
    if (savedLang && savedLang !== 'system' && SUPPORTED_LANGUAGES[savedLang]) {
        currentLanguage = savedLang;
    } else {
        currentLanguage = detectSystemLanguage();
    }
    
    document.documentElement.lang = currentLanguage;
    applyTranslations();
}

/**
 * Henter oversatt tekst for en gitt nøkkel
 * @param {string} key - Oversettelsesnøkkel (f.eks. 'toolbar.back')
 * @param {Object} [params] - Parametre for interpolering (f.eks. {current: 1, total: 5})
 * @returns {string} Oversatt tekst, eller nøkkelen hvis ikke funnet
 */
function t(key, params = {}) {
    const translations = TRANSLATIONS[currentLanguage] || TRANSLATIONS['nb'];
    let text = translations[key] || TRANSLATIONS['nb'][key] || key;
    
    // Interpoler parametre: {current} → verdi
    for (const [param, value] of Object.entries(params)) {
        text = text.replace(`{${param}}`, value);
    }
    
    return text;
}

/**
 * Setter aktivt språk og oppdaterer UI
 * @param {string} langCode - Språkkode ('system' for automatisk, eller 'nb', 'en', etc.)
 */
function setLanguage(langCode) {
    if (langCode === 'system') {
        localStorage.setItem('bare-language', 'system');
        currentLanguage = detectSystemLanguage();
    } else if (SUPPORTED_LANGUAGES[langCode]) {
        localStorage.setItem('bare-language', langCode);
        currentLanguage = langCode;
    } else {
        return;
    }
    
    document.documentElement.lang = currentLanguage;
    applyTranslations();
}

/**
 * Henter gjeldende språkkode
 * @returns {string} Aktiv språkkode
 */
function getCurrentLanguage() {
    return currentLanguage;
}

/**
 * Henter lagret språkpreferanse ('system' eller en kode)
 * @returns {string} Lagret preferanse
 */
function getSavedLanguagePreference() {
    return localStorage.getItem('bare-language') || 'system';
}

/**
 * Anvender alle oversettelser på DOM-elementer med data-i18n attributter
 * og oppdaterer dynamiske UI-elementer
 */
function applyTranslations() {
    // Oppdater elementer med data-i18n attributt (textContent)
    document.querySelectorAll('[data-i18n]').forEach(el => {
        const key = el.getAttribute('data-i18n');
        const translation = t(key);
        if (el.tagName === 'OPTION') {
            el.textContent = translation;
        } else if (el.tagName === 'INPUT') {
            el.placeholder = translation;
        } else {
            el.textContent = translation;
        }
    });
    
    // Oppdater elementer med data-i18n-placeholder
    document.querySelectorAll('[data-i18n-placeholder]').forEach(el => {
        el.placeholder = t(el.getAttribute('data-i18n-placeholder'));
    });
    
    // Oppdater elementer med data-i18n-title
    document.querySelectorAll('[data-i18n-title]').forEach(el => {
        el.title = t(el.getAttribute('data-i18n-title'));
    });
    
    // Oppdater elementer med data-i18n-html (for innhold med HTML)
    // Bruker DOMParser for å unngå direkte innerHTML-tildeling (CodeQL/XSS)
    document.querySelectorAll('[data-i18n-html]').forEach(el => {
        const translated = t(el.getAttribute('data-i18n-html'));
        const parser = new DOMParser();
        const doc = parser.parseFromString(translated, 'text/html');
        el.replaceChildren(...Array.from(doc.body.childNodes));
    });
    
    // Oppdater toolbar-knapper med tooltips (kombinerer tekst + snarvei)
    updateToolbarTooltips();
    
    // Oppdater footer-status hvis "Klar"
    if (elements.footerStatus && 
        (elements.footerStatus.textContent === 'Klar' || 
         elements.footerStatus.textContent === t('footer.ready'))) {
        elements.footerStatus.textContent = t('footer.ready');
    }
    
    // Oppdater tema-knapp
    const settings = getSettings();
    if (settings) {
        let effectiveTheme = settings.theme;
        if (effectiveTheme === 'system') {
            effectiveTheme = window.matchMedia('(prefers-color-scheme: dark)').matches ? 'dark' : 'light';
        }
        updateThemeButton(effectiveTheme);
    }
    
    // Oppdater språk-velger
    const langSelect = document.getElementById('setting-language');
    if (langSelect) {
        const savedPref = getSavedLanguagePreference();
        langSelect.value = savedPref;
    }
}

